//! 논리 연산자
// : boolean 값을 연산하는데 사용

// - && 연산자와 || 연산자
//   : 두 개의 피연산자를 가지는 이항 연산자이며, 피연산자들의 결합 방향은 '왼쪽에서 오른쪽'

// - ! 연산자
//   : 피연산자가 단 하나뿐인 단항 연산자이며, 피연산자의 결합 방향은 '오른쪽에서 왼쪽'

//* 논리 연산자의 종류
// and(논리곱): &&
// : false값이 하나라도 있으면 무조건 false

// or(논리합): ||
// : true값이 하나라도 있으면 무조건 true

// not(부정논리): !

