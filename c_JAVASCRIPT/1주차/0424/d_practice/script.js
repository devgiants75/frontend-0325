//! 문제 2(대입연산자)

// 변수 total이 0으로 시작
// : 아래의 연산 후 total의 값을 콘솔에 출력

let total = 0;

// 1) 5를 추가하세요.
total += 5; // total = total + 5: 5
// 2) 결과값에 3을 곱하세요.
total *= 3; // total = total * 3: 15
// 3) 결과값에서 2를 빼세요.
total -= 2; // total = total - 2: 13
console.log(`최종 결과: ${total}`); // 최종 결과: 13

