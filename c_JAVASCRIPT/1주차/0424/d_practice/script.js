//! 문제 4
// 삼각형의 넓이 계산하기
// 삼각형의 밑변(base)과 높이(height)를 사용자로부터 입력받아 넓이(area)를 계산하고, 그 결과를 콘솔에 출력하는 스크립트를 작성

// >> 삼각형의 넓이: 밑변 * 높이 * 0.5

let base = Number(prompt("삼각형의 밑변을 입력하세요 (cm): ")); // 사용자로부터 밑변을 입력받음
let height = Number(prompt("삼각형의 높이를 입력하세요 (cm): ")); // 사용자로부터 높이를 입력받음
let area = 0.5 * base * height; // 삼각형의 넓이 계산
console.log(`삼각형의 넓이 : ${area}cm²`); // 콘솔에 넓이 출력

//! 문제 5
// 평균 구하기
// 사용자로부터 세 개의 숫자(num1, num2, num3)를 입력받아 이들의 평균(average)을 계산하고, 그 결과를 콘솔에 출력하는 스크립트를 작성

let num1 = Number(prompt('첫 번째 숫자를 입력하세요'));
